package ViniciusLicao;


public class Pessoa {

	public String codigo;
	public String nome;
	public double altura;
	public int idade;
	public String nascimento;
	public String escolaridade;
	public double salario;
	
	public Pessoa(String codigo, String nome, double altura, int idade, String nascimento, String escolaridade, double salario)
	{
		this.codigo = codigo;
		this.nome = nome;
		this.altura = altura;
		this.idade = idade;
		this.nascimento = nascimento;
		this.escolaridade = escolaridade;
		this.salario = salario;
	}
	
	
}